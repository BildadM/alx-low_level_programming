#include <stdio.h>

/**
 * add - sum two numbers
 *
 * @a: int one
 * @b: int two
 *
 * Return: sum
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two numbers
 * @a: int one
 * @b: int two
 *
 * Return: minus
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - mul two numbers
 * @a: int one
 * @b: int two
 *
 * Return: mul
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - div two numbers
 * @a: int one
 * @b: int two
 *
 * Return: div
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - mod of two numbers
 * @a: int one
 * @b: int two
 *
 * Return: mod
 */

int mod(int a, int b)
{
	return (a % b);
}
